#Faça um Programa que peça as 4 notas bimestrais e mostre a média. 
n1 = float(input("Insira sua nota do primeiro bimestre:\n"))
n2 = float(input("Insira sua nota do segundo bimestre:\n"))
n3 = float(input("Insira sua nota do terceiro bimestre:\n"))
n4 = float(input("Insira sua nota do quarto bimestre:\n"))

media = (n1+n2+n3+n4)/4

print("A sua média final é: ", media)
